      <p>The design conference ATTENDING [TO] FUTURES will take place at Köln International School of Design of TH Köln (Cologne, Germany) on 18-20 November 2021, but will be transformed into an online event, if regulations with regard to the Covid-19 pandemic prohibit physical large-scale events.</p>
      <p>Three panels will frame practical and theoretical positions and projects that critically examine the socio-political and ecological consequences of dominant ways of knowing, being, and doing in design and explore the possibilities of subversive practices and alternative teaching-learning scenarios. Lectures, workshops, urban walks, live performances, film screenings, and discussions invite interdisciplinary exchange among designers, activists, researchers, and educators from the fields of design, art, architecture, cultural studies, and anthropology.</p>
      <p>The conference ATTENDING [TO] FUTURES examines the ambivalent role and responsibility of design as world-making at a moment in which the world &laquo;is presented in crisis&raquo; (Ghosn, Jazairy). Acknowledging the ways in which design (as practices, forms of knowledge, and sets of objects) is accountable for social and environmental injustice, the conference invites critical perspectives that scrutinize unchallenged disciplinary norms and dominant ways of knowing, being, doing, and imagining in design education, research, and practice. Drawing on current de-/anti-colonial, post-humanist, queer-feminist and disability discourses, the conference attempts a political reprogramming of design in order to generate transformative perspectives on design education, research, and practice.</p>
      <br>
      <h2>Contested Histories<br>UNLEARNING</h2>
      <p>The first panel critically revises and problematizes the eurocentric, patriarchal, colonial, environmentally untenable, and capitalist traditions in which design is embedded and calls for an &laquo;unlearning&raquo; (Gayatri Spivak) of discriminatory, exclusive, oppressive, and &laquo;defuturing&raquo; (Toni Fry) codes and design practices that came into being because of them. The aim of this panel is to expose normative definitions of design, design epistemologies, and established ways of learning in order to understand ›why we design, what we design‹ (loosely based on Mary Lawhon) and to recognize how design produces inequality and subjugation.</p>
      <br>
      <h2>Radical Futures<br>ATTENDING [TO]</h2>
      <p>The second panel considers design’s inherent demand to explore, test, and produce material-discursive configurations that do not (yet) exist (Séverine Marguin et al.) in order to imagine and constitute radically different worlds and form the basis of future design (Daniel Gethmann et al.). Through this lens, the future is addressed as a present &laquo;space of becoming&raquo; inclusive, empathetic, just, and sustainable (Bill Ashcroft). Designing the future means both attending, i.e. active participation in, and attending to, i.e. taking responsibility and care for, creative ways of living together with people, things, animals, and plants. All future-making strategies require asking whether designs of new possible worlds leave social, political, and economical power structures untouched or whether they undermine and change them.</p>
      <br>
      <h2>Critical Practices<br>LEADING OUT</h2>
      <p>The third panel proceeds from the etymology of education: latin ›educare‹ not as ›e-ducere‹—i.e. ›leading someone to something‹—but as ›ex-ducere‹, ›leading someone out‹ of the familiar (Tim Ingold). Attention to the constitutive conditions of design requires a reckoning with a multiplicity of actors and contexts, from institutional norms and regulations, to pedagogies, curricula, materials, architectural environments, and discursive protocols. This panel therefore focuses on and calls for the transformation of everything that helps bring design into being: physical and digital tools, as well as their material infrastructures and legitimating disciplinary narratives.</p>
